     * Create a new message instance.
     *
     * @param User $user
     */
    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {

    //     return $this->subject('Mail from Real Programmer')
    //     ->view('email.birthday')
    //    ;

        return $this->view('email.birthday')
            ->cc(['alisa.kike@landmark.al']);
    }
}
